create table квалификация (
id serial primary key,
название varchar(100),
описание varchar (300)
);

create table специальность (
id serial primary key,
полнове_название varchar (100),
сокращенное_названиие varchar (100),
id_квалификация int,
foreign key(id_квалификация) references квалификация(id)
);

create type уровень_образования as enum ('Основное общее','Среднее общее','Среднее профессиональное','Высшее');

create table образование (
id serial primary key,
уровень_образования уровень_образования,
серия int,
номер int,
регистрационный_номер int,
дата_выдачи date,
id_специальность int,
foreign key(id_специальность) references специальность(id)
);

create table документ (
id serial primary key,
серия int,
номер int,
дата_выдачи date,
кем_выдан varchar(50)
);

create table сотрудник (
id serial primary key,
фамилия varchar(100),
имя varchar(100),
отчество varchar(100),
номер_телефона varchar(100),
дата_рождения date,
снилс varchar(100),
инн varchar(100),
id_паспорт int,
стаж_работы int,
семейное_положение boolean,
дата_приема date,
дата_увольнения date,
статус boolean default false,
foreign key(id_паспорт) references документ(id)
);

create table место_обучения (
id serial primary key,
полное_название varchar (150),
сокращенное_названеие varchar(150)
);

create type вид as enum ('очный','заочный');

create table обучение (
id serial primary key,
название varchar(100),
вид вид,
дата_начала date,
дата_окончания date,
формат_проведени boolean,
id_место_проведения int,
foreign key(id_место_проведения) references место_обучения(id)
);

create table обучение_сотрудника (
id serial primary key,
id_обучение int,
id_сотрудник int,
обучение_пройдено boolean,
путь_документа varchar(100),
номер_документа varchar(100),
foreign key(id_обучение) references обучение(id),
foreign key(id_сотрудник) references сотрудник(id)
);

create table должность (
id serial primary key,
название varchar(100),
описание_трудовых_обязательностей varchar(100)
);

create type отдел as enum ('Бухгалтерия', 'IT', 'Маркетинг', 'Отдел продаж');

create table должность_сотрудника (
id serial primary key,
id_должность int,
id_сотрудник int,
отдел отдел,
foreign key(id_должность) references должность(id),
foreign key(id_сотрудник) references сотрудник(id)
);

create table образование_сотрудника (
id_сотрудник int,
id_образование int,
primary key(id_сотрудник, id_образование),
foreign key (id_сотрудник) references сотрудник(id) on delete cascade,
foreign key (id_образование) references образование(id) on delete cascade
);


